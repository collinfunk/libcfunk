static void
generate_table (void)
{
  int i;

  for (i = 0; i < 256; ++i)
    {
      int value;
      if (i >= '0' && i <= '9')
        value = (i - '0') + 52;
      else if (i >= 'A' && i <= 'Z')
        value = (i - 'A');
      else if (i >= 'a' && i <= 'z')
        value = (i - 'a') + 26;
      else if (i == '+')
        value = 62;
      else if (i == '/')
        value = 63;
      else
        value = -1;
      base64_decode_table[i] = value;
    }
}

static void
print_table (void)
{
  int i;

  printf ("static const signed char base64_decode_table[256] = {\n");
  for (i = 0; i < 256; ++i)
    printf ("%d, ", base64_decode_table[i]);
  printf ("};\n");
}
