#include <config.h>

#include <time.h>

/* Return the number of days in a given year. This function follows the
   Gregorian calendar rule for calculating leap years:

     Every year that is exactly divisible by 4 is a leap year. The exception
     to this rule is that centurial years that are not exactly divisible by
     400 are leap years.
 */
int
dysize (int year)
{
  return (year & 3) ? 365 : (year % 100) ? 366 : (year % 400) ? 365 : 366;
}
