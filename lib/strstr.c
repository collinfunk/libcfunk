#include <config.h>

#include <stddef.h>
#include <string.h>

char *
strstr (const char *s1, const char *s2)
{
  size_t s2_len;

  if (*s2 == '\0')
    return (char *) s1;

  s2_len = strlen (s2);
  for (;;)
    {
      const char *p = strchr (s1, *s2);
      if (p == NULL)
        return NULL;
      if (strncmp (p, s2, s2_len) == 0)
        return (char *) p;
      else
        ++s1;
    }

  return NULL;
}
